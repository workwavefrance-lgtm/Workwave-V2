export default function BottomBar() {
  const pathname = usePathname();
  const [showMore, setShowMore] = useState(false);

  function isActive(href: string) {
    if (href === "/pro/dashboard") return pathname === href;
    return pathname.startsWith(href);
  }

  const moreActive = MORE_ITEMS.some((item) => isActive(item.href));

  return (
    <>
      {/* Overlay pour fermer le menu "Plus" */}
      {showMore && (
        <div
          className="fixed inset-0 z-40 lg:hidden"
          onClick={() => setShowMore(false)}
        />
      )}

      {/* Menu "Plus" */}
      {showMore && (
        <div className="fixed bottom-20 right-4 z-50 lg:hidden bg-[var(--bg-secondary)] border border-[var(--border-color)] rounded-2xl shadow-lg p-2 min-w-[180px]">
          {MORE_ITEMS.map((item) => (
            <Link
              key={item.href}
              href={item.href}
              onClick={() => setShowMore(false)}
              className={`flex items-center gap-3 px-4 py-3 rounded-xl text-sm font-medium transition-all duration-250 ${
                isActive(item.href)
                  ? "bg-[var(--accent-muted)] text-[var(--accent)]"
                  : "text-[var(--text-secondary)] hover:bg-[var(--bg-tertiary)]"
              }`}
            >
              {item.icon}
              {item.label}
            </Link>
          ))}
        </div>
      )}

      {/* Bottom bar */}
      <nav className="fixed bottom-0 left-0 right-0 z-40 lg:hidden bg-[var(--bg-primary)]/90 backdrop-blur-xl border-t border-[var(--border-color)]">
        <div className="flex items-center justify-around h-16 pb-[env(safe-area-inset-bottom)]">
          {MAIN_ITEMS.map((item) => {
            const active = isActive(item.href);
            return (
              <Link
                key={item.href}
                href={item.href}
                className={`flex flex-col items-center gap-1 py-1 px-2 transition-colors duration-250 ${
                  active ? "text-[var(--accent)]" : "text-[var(--text-tertiary)]"
                }`}
              >
                {item.icon}
                <span className="text-[10px] font-medium">{item.label}</span>
              </Link>
            );
          })}

          {/* Bouton Plus */}
          <button
            onClick={() => setShowMore(!showMore)}
            className={`flex flex-col items-center gap-1 py-1 px-2 transition-colors duration-250 ${
              moreActive || showMore
                ? "text-[var(--accent)]"
                : "text-[var(--text-tertiary)]"
            }`}
          >
            <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
              <path strokeLinecap="round" strokeLinejoin="round" d="M6.75 12a.75.75 0 11-1.5 0 .75.75 0 011.5 0zM12.75 12a.75.75 0 11-1.5 0 .75.75 0 011.5 0zM18.75 12a.75.75 0 11-1.5 0 .75.75 0 011.5 0z" />
            </svg>
            <span className="text-[10px] font-medium">Plus</span>
          </button>
        </div>
      </nav>
    </>
  );
}
